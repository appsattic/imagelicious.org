// --------------------------------------------------------------------------------------------------------------------

var store = {
  hash : null,
}

// --------------------------------------------------------------------------------------------------------------------

function setUser(user) {
  store.user = user
}

function setHash(hash) {
  store.hash = hash
}

function extract() {
  return JSON.parse(JSON.stringify(store))
}

// --------------------------------------------------------------------------------------------------------------------

module.exports = {
  setUser,
  setHash,
  extract,
}

